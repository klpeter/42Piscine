#include <unistd.h>

int	main(void)
{
	int	e;
	int	u;

	e = 'z';
	u = 'Y';
	while(e >= 'a' || u >= 'A')
	{
		if (e % 2 == 0)
		{
			write(1, &e, 1);
		}
		e--;
		if (u % 2 != 0)
		{
			write(1, &u, 1);
		}
		u--;
	}
}