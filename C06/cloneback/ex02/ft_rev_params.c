#include <unistd.h>

void	ft_print_params(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	while (argc > 1)
	{
		ft_print_params(argv[argc - 1]);
		write(1, "\n", 1);
		argc--;
	}
	return (0);
}
